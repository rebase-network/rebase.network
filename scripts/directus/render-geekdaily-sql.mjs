import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { renderInsert, sqlJson, sqlString } from './sql-utils.mjs';

const sourceArg = process.argv[2] ?? 'geekdaily.csv';
const sourcePath = resolve(sourceArg);
const outputPath = 'infra/directus/sql/003_geekdaily_archive.sql';

function parseCsv(text) {
  const rows = [];
  let field = '';
  let row = [];
  let inQuotes = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (inQuotes) {
      if (char === '"' && next === '"') {
        field += '"';
        index += 1;
      } else if (char === '"') {
        inQuotes = false;
      } else {
        field += char;
      }
      continue;
    }

    if (char === '"') {
      inQuotes = true;
      continue;
    }

    if (char === ',') {
      row.push(field);
      field = '';
      continue;
    }

    if (char === '\n') {
      row.push(field);
      field = '';
      rows.push(row);
      row = [];
      continue;
    }

    if (char === '\r') {
      continue;
    }

    field += char;
  }

  if (field.length > 0 || row.length > 0) {
    row.push(field);
    rows.push(row);
  }

  return rows;
}

function normalizeHeader(value) {
  return value.replace(/^\uFEFF/, '').trim();
}

function parseDate(value) {
  const [year, month, day] = value.split('/').map((part) => Number(part));
  return new Date(Date.UTC(year, month - 1, day, 8, 0, 0)).toISOString();
}

function buildSummary(items) {
  const previewTitles = items.slice(0, 3).map((item) => item.title).join('、');
  const suffix = items.length > 3 ? ' 等内容。' : '。';
  return `本期收录 ${items.length} 条社区推荐，涉及 ${previewTitles}${suffix}`;
}

function buildBody(episodeNumber, items) {
  const contributors = [...new Set(items.map((item) => item.author).filter(Boolean))];
  const intro = `极客日报#${episodeNumber} 收录了 ${items.length} 条来自社区的推荐${contributors.length > 0 ? `，推荐人包括 ${contributors.join('、')}` : ''}。`;
  const lines = items.map((item, index) => {
    const sourceLine = item.sourceUrl ? `\n来源：${item.sourceUrl}` : '';
    return `${index + 1}. ${item.title}\n推荐人：${item.author || 'unknown'}${sourceLine}\n摘要：${item.summary}`;
  });

  return [intro, ...lines].join('\n\n');
}

function loadEpisodes() {
  const raw = readFileSync(sourcePath, 'utf8');
  const rows = parseCsv(raw);

  if (rows.length < 2) {
    throw new Error(`CSV file ${sourcePath} does not contain data rows.`);
  }

  const headers = rows[0].map(normalizeHeader);
  const records = rows.slice(1).filter((row) => row.some((cell) => cell.trim() !== '')).map((row) => {
    const record = Object.fromEntries(headers.map((header, index) => [header, row[index] ?? '']));
    return {
      episode: Number(String(record.episode).replace('#', '').trim()),
      time: record.time.trim(),
      title: record.title.trim(),
      author: record.author.trim(),
      url: record.url.trim(),
      introduce: record.introduce.trim(),
    };
  });

  const episodeMap = new Map();

  for (const record of records) {
    if (!Number.isFinite(record.episode)) {
      continue;
    }

    if (!episodeMap.has(record.episode)) {
      episodeMap.set(record.episode, {
        episodeNumber: record.episode,
        slug: `geekdaily-${record.episode}`,
        status: 'published',
        title: `极客日报#${record.episode}`,
        publishedAt: parseDate(record.time),
        items: [],
      });
    }

    episodeMap.get(record.episode).items.push({
      title: record.title,
      author: record.author,
      sourceUrl: record.url,
      summary: record.introduce,
    });
  }

  return [...episodeMap.values()]
    .sort((left, right) => right.episodeNumber - left.episodeNumber)
    .map((episode, index) => ({
      ...episode,
      sort: index + 1,
      summary: buildSummary(episode.items),
      body: buildBody(episode.episodeNumber, episode.items),
      tags: [],
    }));
}

const episodes = loadEpisodes();

const sql = [
  '-- Generated by scripts/directus/render-geekdaily-sql.mjs',
  `-- Source: ${sourceArg}`,
  'begin;',
  '',
  'delete from public.geekdaily_episodes;',
  '',
  renderInsert({
    table: 'public.geekdaily_episodes',
    columns: [
      'status',
      'sort',
      'date_created',
      'date_updated',
      'episode_number',
      'slug',
      'title',
      'summary',
      'body',
      'published_at',
      'tags',
      'items',
    ],
    rows: episodes.map((episode) => ({
      status: sqlString(episode.status),
      sort: String(episode.sort),
      date_created: sqlString(episode.publishedAt),
      date_updated: sqlString(episode.publishedAt),
      episode_number: String(episode.episodeNumber),
      slug: sqlString(episode.slug),
      title: sqlString(episode.title),
      summary: sqlString(episode.summary),
      body: sqlString(episode.body),
      published_at: sqlString(episode.publishedAt),
      tags: sqlJson(episode.tags),
      items: sqlJson(episode.items),
    })),
    onConflict: 'on conflict do nothing',
  }),
  '',
  'commit;',
  '',
].join('\n');

mkdirSync('infra/directus/sql', { recursive: true });
writeFileSync(outputPath, `${sql.trim()}\n`);
console.log(`wrote ${outputPath} with ${episodes.length} episodes`);
