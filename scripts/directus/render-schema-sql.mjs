import { mkdirSync, writeFileSync } from 'node:fs';
import { collections, directusPolicy } from './schema-config.mjs';
import { renderInsert, sqlJsonText, sqlString } from './sql-utils.mjs';

const outputPath = 'infra/directus/sql/001_schema.sql';

function renderCreateTable(collection) {
  const columns = collection.fields.map((field) => `  ${field.column}`);
  const constraints = (collection.checks ?? []).map((check) => `  ${check}`);
  const lines = [...columns, ...constraints].join(',\n');

  return `create table if not exists public.${collection.name} (\n${lines}\n);`;
}

function renderFieldRows() {
  const rows = [];

  for (const collection of collections) {
    collection.fields.forEach((field, index) => {
      rows.push({
        collection: sqlString(collection.name),
        field: sqlString(field.field),
        special: sqlString(field.special),
        interface: sqlString(field.interface),
        options: sqlJsonText(field.options),
        readonly: field.readonly ? 'true' : 'false',
        hidden: field.hidden ? 'true' : 'false',
        sort: String(field.sort ?? index + 1),
        width: sqlString(field.width ?? 'full'),
        translations: 'null',
        note: sqlString(field.note),
        conditions: 'null',
        required: field.required ? 'true' : 'false',
        searchable: field.searchable === false ? 'false' : 'true',
      });
    });
  }

  return rows;
}

function renderCollectionRows() {
  return collections.map((collection, index) => ({
    collection: sqlString(collection.name),
    icon: sqlString(collection.icon),
    note: sqlString(collection.note),
    display_template: 'null',
    hidden: collection.hidden ? 'true' : 'false',
    singleton: collection.singleton ? 'true' : 'false',
    translations: 'null',
    archive_field: sqlString(collection.archiveField),
    archive_app_filter: 'true',
    archive_value: sqlString(collection.archiveValue),
    unarchive_value: sqlString(collection.unarchiveValue),
    sort_field: sqlString(collection.sortField),
    accountability: sqlString(collection.accountability ?? 'all'),
    color: sqlString(collection.color),
    item_duplication_fields: 'null',
    sort: String(index + 1),
    group: 'null',
    collapse: sqlString('open'),
    preview_url: 'null',
    versioning: 'false',
  }));
}

function renderPermissionRows() {
  return collections.map((collection) => ({
    collection: sqlString(collection.name),
    action: sqlString('read'),
    permissions: sqlJsonText(collection.singleton ? null : { status: { _eq: 'published' } }),
    validation: 'null',
    presets: 'null',
    fields: sqlString('*'),
    policy: sqlString(directusPolicy.policyId),
  }));
}

const collectionNames = collections.map((collection) => sqlString(collection.name)).join(', ');

const sql = [
  '-- Generated by scripts/directus/render-schema-sql.mjs',
  'create extension if not exists pgcrypto;',
  '',
  '-- Clean and re-register Directus metadata for the project collections.',
  `delete from directus_access where policy = ${sqlString(directusPolicy.policyId)} or "user" = ${sqlString(directusPolicy.userId)};`,
  `delete from directus_users where id = ${sqlString(directusPolicy.userId)} or email = ${sqlString(directusPolicy.userEmail)};`,
  `delete from directus_permissions where policy = ${sqlString(directusPolicy.policyId)};`,
  `delete from directus_fields where collection in (${collectionNames});`,
  `delete from directus_relations where many_collection in (${collectionNames}) or one_collection in (${collectionNames});`,
  '',
  ...collections.map(renderCreateTable),
  '',
  ...collections.flatMap((collection) => (collection.indexes ?? []).map((statement) => `${statement};`)),
  '',
  renderInsert({
    table: 'directus_collections',
    columns: [
      'collection',
      'icon',
      'note',
      'display_template',
      'hidden',
      'singleton',
      'translations',
      'archive_field',
      'archive_app_filter',
      'archive_value',
      'unarchive_value',
      'sort_field',
      'accountability',
      'color',
      'item_duplication_fields',
      'sort',
      'group',
      'collapse',
      'preview_url',
      'versioning',
    ],
    rows: renderCollectionRows(),
    onConflict: `on conflict (collection) do update set
  icon = excluded.icon,
  note = excluded.note,
  display_template = excluded.display_template,
  hidden = excluded.hidden,
  singleton = excluded.singleton,
  translations = excluded.translations,
  archive_field = excluded.archive_field,
  archive_app_filter = excluded.archive_app_filter,
  archive_value = excluded.archive_value,
  unarchive_value = excluded.unarchive_value,
  sort_field = excluded.sort_field,
  accountability = excluded.accountability,
  color = excluded.color,
  item_duplication_fields = excluded.item_duplication_fields,
  sort = excluded.sort,
  "group" = excluded."group",
  collapse = excluded.collapse,
  preview_url = excluded.preview_url,
  versioning = excluded.versioning`,
  }),
  '',
  renderInsert({
    table: 'directus_fields',
    columns: [
      'collection',
      'field',
      'special',
      'interface',
      'options',
      'readonly',
      'hidden',
      'sort',
      'width',
      'translations',
      'note',
      'conditions',
      'required',
      'searchable',
    ],
    rows: renderFieldRows(),
    onConflict: 'on conflict do nothing',
  }),
  '',
  `insert into directus_policies (id, name, icon, description, enforce_tfa, admin_access, app_access)
values (
  ${sqlString(directusPolicy.policyId)},
  'website read policy',
  'language',
  'Read-only access for the Rebase public website.',
  false,
  false,
  false
)
on conflict (id) do update set
  name = excluded.name,
  icon = excluded.icon,
  description = excluded.description,
  enforce_tfa = excluded.enforce_tfa,
  admin_access = excluded.admin_access,
  app_access = excluded.app_access;`,
  '',
  `insert into directus_users (id, first_name, last_name, email, status, token, provider)
values (
  ${sqlString(directusPolicy.userId)},
  'Website',
  'API',
  ${sqlString(directusPolicy.userEmail)},
  'active',
  ${sqlString('${DIRECTUS_WEBSITE_TOKEN}')},
  'default'
)
on conflict (id) do update set
  first_name = excluded.first_name,
  last_name = excluded.last_name,
  email = excluded.email,
  status = excluded.status,
  token = excluded.token,
  provider = excluded.provider;`,
  '',
  `insert into directus_access (id, "user", policy)
values (
  ${sqlString(directusPolicy.accessId)},
  ${sqlString(directusPolicy.userId)},
  ${sqlString(directusPolicy.policyId)}
)
on conflict (id) do update set
  "user" = excluded."user",
  policy = excluded.policy;`,
  '',
  renderInsert({
    table: 'directus_permissions',
    columns: ['collection', 'action', 'permissions', 'validation', 'presets', 'fields', 'policy'],
    rows: renderPermissionRows(),
    onConflict: 'on conflict do nothing',
  }),
  '',
].join('\n');

mkdirSync('infra/directus/sql', { recursive: true });
writeFileSync(outputPath, `${sql.trim()}\n`);
console.log(`wrote ${outputPath}`);
